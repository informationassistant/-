function askQuestion() {
    const input = document.getElementById('userInput');
    const text = input.value.toLowerCase().trim();
    if (!text) return;

    addMessage(input.value, 'user');
    
    let answer = "К сожалению, я пока не знаю ответа на этот вопрос. Попробуй спросить про 'Бит', 'рекурсия' или 'Графы'.";

    // Поиск совпадений в базе знаний
    for (let key in knowledgeBase) {
        if (text.includes(key)) {
            answer = knowledgeBase[key];
            break;
        }
    }

    setTimeout(() => addMessage(answer, 'bot'), 500);
    input.value = '';
}

function addMessage(text, side) {
    const container = document.getElementById('chatContainer');
    const msgDiv = document.createElement('div');
    msgDiv.className = `message ${side}`;
    msgDiv.innerText = text;
    container.appendChild(msgDiv);
    container.scrollTop = container.scrollHeight;
}

function setQuestion(text) {
    document.getElementById('userInput').value = text;
    askQuestion();
}

// Позволяет нажимать Enter для отправки
document.getElementById('userInput').addEventListener('keypress', (e) => {
    if (e.key === 'Enter') askQuestion();
});